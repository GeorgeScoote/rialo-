/** 翻译函数：支持参数替换，回退顺序 lang → en → key */
export function t(
  lang: LanguageCode,
  key: string,
  params: Record<string, string | number> = {}
): string {
  let text = (translations[lang] as Record<string, string>)?.[key] || (translations.en as Record<string, string>)[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  });
  return text;
}

/** 根据浏览器语言推断界面语言，默认 en */
export function getBrowserLanguage(): LanguageCode {
  const browserLang = navigator.language.split('-')[0];
  return (browserLang in LANGS ? browserLang : 'en') as LanguageCode;
}

export default translations;
